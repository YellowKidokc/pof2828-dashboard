export const getAxiomById = (id: string): Axiom | undefined => {
  return axioms.find(a => a.id === id);
};

export const getGraphData = (axiomId: string): { nodes: GraphNode[]; edges: GraphEdge[] } => {
  const axiom = getAxiomById(axiomId);
  if (!axiom) return { nodes: [], edges: [] };

  const nodes: GraphNode[] = [
    { id: axiom.id, title: axiom.title, x: 150, y: 100, type: 'current' },
    ...axiom.dependencies.map((depId, i) => {
      const dep = getAxiomById(depId);
      return {
        id: depId,
        title: dep?.title || depId,
        x: 50 + i * 80,
        y: 50,
        type: 'dependency' as const,
      };
    }),
    ...axiom.enables.map((en, i) => ({
      id: en.id,
      title: en.title,
      x: 50 + i * 80,
      y: 150,
      type: 'dependent' as const,
    })),
  ];

  const edges: GraphEdge[] = [
    ...axiom.dependencies.map(depId => ({ from: depId, to: axiom.id, type: 'depends' as const })),
    ...axiom.enables.map(en => ({ from: axiom.id, to: en.id, type: 'enables' as const })),
  ];

  return { nodes, edges };
};
